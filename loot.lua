    local fullRaid = {}
    local playerSubgroup = nil
    local playerName = UnitName("Player")

    for i=1,40 do
        local name, rank, subGroup = GetRaidRosterInfo(i)
        if name then
            --[[if name == playerName then -- only include players in the current subgroup
